package com.example.sensitive_coach;

public class RoutineItem {
    private int num ;
    private String name ;
    private String desc ;

    public void setNum(int num) {
        this.num = num ;
    }
    public void setName(String name) {
        this.name = name ;
    }
    public void setDesc(String desc) {
        this.desc = desc ;
    }

    public int getNum() {
        return this.num ;
    }
    public String getName() {
        return this.name ;
    }
    public String getDesc() {
        return this.desc ;
    }
}
